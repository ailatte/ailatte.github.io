function drawCanvas(prefix, name, number, color){
    let id = prefix + name + "Canvas";
    let c = document.getElementById(id);
    let ctx = c.getContext("2d");
    let w = c.width;
    let h = c.height;
    ctx.clearRect(0, 0, w, h);  // 重置画布
    c.style.backgroundColor = color;
    if (number == "0"){
        ctx.fillStyle = "#fff";
        ctx.fillRect(80,0,40,30);
    }
}

