function selectDate(){
    let lyYearId = $('#lyYear');
    let lyMonthId = $('#lyMonth');
    let lyDayId = $('#lyDay');
    let lyHourId = $('#lyHour');
    optionsClear(lyYearId);
    optionsClear(lyMonthId);
    optionsClear(lyDayId);
    optionsClear(lyHourId);
    let nowDate = new Date();
    let year = nowDate.getFullYear();
    let month = nowDate.getMonth() + 1;
    let monthSys = nowDate.getMonth();
    let day = nowDate.getDate();
    let hour = nowDate.getHours();
    for(let i=(year-4); i<(year+5); i++){lyYearId.append("<option value='"+i+"'>"+i+" 年</option>");}
    for(let i=1; i<13; i++){lyMonthId.append("<option value='"+i+"'>"+i+" 月</option>");}
    for(let i=0; i<24; i++){lyHourId.append("<option value='"+i+"'>"+i+" 时</option>");}
    $("#lyYear option[value='"+year+"']").prop("selected", true);
    $("#lyMonth option[value='"+month+"']").prop("selected", true);
    $("#lyHour option[value='"+hour+"']").prop("selected", true);
    let num = MONTHHEAD[monthSys];
    if(monthSys == 1 && checkYear(lyYearId.val())){num++}
    writeDay(num);
    $("#lyDay option[value='"+day+"']").prop("selected", true);
    lyYearId.on('change', function(){
        let value = $(this).val();
        let monthVal = lyMonthId.val();
        if(monthVal == ""){optionsClear(lyDayId);}
        let num = MONTHHEAD[monthVal - 1];
        if(monthVal == 2 && checkYear(value)){num++;}
        writeDay(num)
    });
    lyMonthId.on('change', function(){
        let value = $(this).val();
        let yearVal = lyYearId.val();
        if(yearVal == ""){optionsClear(lyDayId);}
        let num = MONTHHEAD[value - 1];
        if(value == 2 && checkYear(yearVal)){num++;}
        writeDay(num)
    });
}
function writeDay(num){
    let sd = $('#lyDay');
    optionsClear(sd);
    for (let i=1; i<(num+1); i++){sd.append("<option value='"+i+"'>"+i+" 日</option>");}
}
// 判断平年闰年
function checkYear(year){return(0 == year%4 && (year%100 !=0 || year%400 == 0));}
// 重置日期选项
function optionsClear(e){e[0].options.length = 0;}

